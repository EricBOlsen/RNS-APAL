// Print x in binary format, numbits number of digits formatting, including feature to suppress leading '0' digits
void print_bin(__int64 x, int numbits, int suppress_leading)
{
unsigned long long mask = 0x01;

	mask = mask << (numbits - 1);

	do {

		if(x & mask) {
			printf("1");
			suppress_leading = 0;
		}
		else {
			if(!suppress_leading) {
				printf("0");
			}
			else if(numbits == 1) {
				printf("0");
			}

		}

		mask = mask >> 1;
		numbits -= 1;

	} while(numbits);

}


