// get the binary mask with a one set in the number of rightmost 'bits' number of bits
unsigned int get_binary_mask(int bits)
{
unsigned int i;
unsigned int mask = 0;

	
	if(bits <= 0) {
		return (0);
	}

	for(i=0; i<bits; i++) {

		mask = (mask << 1) | 1;

	}

	return(mask);

}

