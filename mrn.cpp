__int64 MRN::Decimal64(void)
{
int seeded = 0;
int size = MRn.size();
__int64 val=0;


	for(int i=size-1; i>=0; i--) {
		
		if(!MRn[i]->Skip) {
			if(!seeded) {
				val = MRn[i]->Digit;
				seeded = 1;
			}
			else {
				val *= MRn[i]->Power;
				val += MRn[i]->Digit;
			}
		}
	}
	
	return(val);

}

